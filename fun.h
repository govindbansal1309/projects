#ifndef __FUN_H__
#define __FUN_H__
/**
 * @brief function that returns sum of 2 numbers
 * 
 * @param number1 
 * @param number2 
 * @return int 
 */
int sum(int number1, int number2)
{
   return number1 + number2;
}
/**
 * @brief function that returns square of a number
 * 
 * @param number 
 * @return int 
 */
int square(int number)
{
   return number * number;
}
#endif  /* __FUN_H__  */